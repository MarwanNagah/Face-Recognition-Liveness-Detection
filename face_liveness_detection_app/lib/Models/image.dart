class Image {
  int _imageID;
  String _path;
  double _width;
  double _height;

  set imageID(int imageID) {
    this._imageID = imageID;
  }

  set path(String path) {
    this._path = path;
  }

  set width(double width) {
    this._width = width;
  }

  set height(double height) {
    this._height = height;
  }
}
